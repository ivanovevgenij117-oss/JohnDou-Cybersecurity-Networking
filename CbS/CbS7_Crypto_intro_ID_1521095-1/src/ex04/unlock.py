# 1. Наш зашифрованный текст и ключ
encrypted = '"4Rj\x12(\\_^(,{\x00c\r,OS]e$j\x18,Dl'
key = 'qwertyuioplkjhgfdsazlfmhkb'

# Превращаем строку в список букв, чтобы удобно их менять
x = list(encrypted)
length = len(x)

print(f"Исходный зашифрованный текст: {''.join(x)}\n")
print("--- Процесс «размотки» (с конца в начало) ---")

# 2. Двигаемся обратно: от последнего шага к первому
# (от length-1 вниз до 0)
for n in range(length - 1, -1, -1):
    
    # Применяем XOR к буквам от позиции n до самого конца
    for i in range(n, length):
        # Находим нужную букву ключа
        key_char = key[i - n]
        # Проводим XOR и возвращаем символ
        x[i] = chr(ord(x[i]) ^ ord(key_char))
    
    # Показываем, как меняется строка на каждом шаге
    # (Вы увидите, как буквы справа налево «оживают»)
    print(f"Шаг n = {n:2}: {''.join(x)}")

print("\n--- Результат ---")
print(f"Флаг успешно восстановлен: {''.join(x)}")
